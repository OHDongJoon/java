package com.lec.condition;

public class Ex03_switch {
	public static void main(String[] args) {
		int num1 = 2;
		switch(num1) {//정수형, 문자형, 문자열 만 가능
			case 1 : System.out.print("1입니다");break;
			case 2 : System.out.print("2입니다");break;
			case 3 : System.out.print("3입니다");break;
			default : System.out.print("그 외 수입니다.");break;
		
//		if(num1==1) {
//			System.out.println("1입니다");
		
	}

}
}
